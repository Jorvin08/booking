private void loadFunctionLogsToTable() {
    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    model.setRowCount(0); // Clear existing rows

    String url = "jdbc:mysql://localhost:3306/booking";
    String username = "root";
    String password = "";

    String query = "SELECT id, user_id, function_name, log_time FROM function_logs ORDER BY log_time DESC";

    try (Connection conn = DriverManager.getConnection(url, username, password);
         Statement stmt = conn.createStatement();
         ResultSet rs = stmt.executeQuery(query)) {

        while (rs.next()) {
            int id = rs.getInt("id");
            int userId = rs.getInt("user_id");
            String functionName = rs.getString("function_name");
            Timestamp logTime = rs.getTimestamp("log_time");

            model.addRow(new Object[]{id, userId, functionName, logTime});
        }

    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, "Failed to load logs: " + ex.getMessage(), "Database Error", JOptionPane.ERROR_MESSAGE);
    }
}

   
